package array;

// 학생의 점수를 출력 (배열이 필요한 이유)
public class Array1 {
    public static void main(String[] args) {
        int std1 = 90;
        int std2 = 80;
        int std3 = 70;
        int std4 = 60;
        int std5 = 50; // 만약 학생이 더 많으면??

        System.out.println("학생1 점수: " + std1);
        System.out.println("학생2 점수: " + std2);
        System.out.println("학생3 점수: " + std3);
        System.out.println("학생4 점수: " + std4);
        System.out.println("학생5 점수: " + std5);
    }
}
