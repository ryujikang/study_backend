package variable;

public class Var7 {
    public static void main(String[] args) {
        // ~타입(type), ~형식, ~형
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언 : true, false 입력 가능
        char d = 'A'; // 문자 하나, 작은따옴표(')
        String e = "Hello Java"; // 문자열, 문자열을 다루기 위한 특별한 타입, 큰따옴표(")

        // 리터럴(literal) : 개발자가 직접 적은 고정된 값
        // 정수 리터럴, 실수 리터럴 ...

        // Ctrl + D : 아래줄로 복사 가능
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
